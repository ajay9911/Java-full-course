package conditional_statement;

public class IfElse {
    public static void main(String[] args) {
        boolean isMale=false;

        String name="Ajay";
        if (isMale){
            System.out.println("Mr."+name);
        }
        else {
            System.out.println("Ms."+name);

        }

        System.out.println(name);
    }
}
